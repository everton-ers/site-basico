   <br><br>
 
 <b>No Caminho de Santiago de Compostela, primeiras impressões </b>
 
 <br><br>
 
Existe a alguns seculos na Espanha o costume de pessoas de
diversos tipos e nacionalidades percorrerem o que convencionou-se a chamar
``O Caminho de Santiago de Compostela``.

<br><br>

Com o passar dos seculos e muitas historias nas costas, o Caminho a
Santiago de Compostela (nome de uma cidade ao noroeste da Espanha, na
regiao da Galicia, proxima a Portugal) obteve cinco ou seis rotas para se
chegar ao destino, sendo as maneiras de ``empezar``: a pé ou de bicicleta. O
mais famoso trajeto é o caminho frances, comecando na cidade francesa de
St Jean-Pied-de-Port e apos 760 Km de caminhada ou pedalada finalizando na
cidade de Santiago de Compostela. Esta é a rota mais famosa e percorrida,
e há em livrarias, lojas e na internet inumeros guias e mapas de todos os
tipos com informacoes sobre as rotas a percorrer: quilometragem entre os
pueblos, clima na epoca, elevacao do terreno, infraestrutura dos
albergues, sinalizacao no caminho e informacoes turisticas de importancia.

<br><br>

Há ate algumas pessoas que passam de Santiago de Compostela e vao a
Finisterra\index{Finisterra}, na costa da Galicia\index{Galícia}, local onde na epoca medieval
acreditava-se ser o fim da Terra, dai o nome Finisterra. Sao mais três
dias a pe ate Finisterra.

<br><br>

Estou fazendo o Caminho de Santiago\index{Caminho de Santiago} a pe e hoje estou em Leon, uma das
tres cidades mais importantes no caminho frances. Hoje sao dois tercos do
caminho completos, esperamos chegar dia 23 de setembro a Santiago de
Compostela\index{Santiago de Compostela}, enfim.

<br><br>

Em nosso caso, faço a caminhada com meu pai e minha irma, serao no total
30 dias de caminhada entre os pueblos da Espanha\index{Espanha}, suas plantacoes de uva e
oliveiras, igrejas medievais e companhias distintas e divertidas de
pessoas da Alemanha, França, Espanha e Italia em sua maioria.

<br><br>

Ate o momento tres fatos me impressionaram, ou somente sobre estes tres fatos
quero escrever, e a ordem de exposicao não importa:

<br><br>

<b>1)</b> É normal encontrarmos nos pueblos fontes de agua potavel, de graca,
disponivel para os peregrinos abastecerem suas garrafas. Pueblos sao
aldeias organizadas e com boa infraestrutura, com 20 a 500 habitantes, que
tem aos montes pelo Caminho de Santiago. É um conceito de ``cidade`` para
pequena quantidade de pessoas inexistente no Brasil. Desta forma, estando proximo ao pueblo de Irache\index{Irache} pensava que as fontes
eram so de agua potavel. Mas a surpresa foi encontrar proximo ao mosteiro
local uma fonte de vinho, a vontade, e com uma bela de uma placa
solicitando que o peregrino\index{peregrino} em direcao a Santiago, que sirva-se. Bom,
tenho fotos, troquei minhas garrafas de agua por vinho\index{vinho}, e fui bebendo pelo
resto da caminhada naquele dia. É dito que aquela é a unica fonte de vinho
no mundo, gratis.

<br><br>

<b>2)</b> Todos os dias, a depender de nosso plano de quilometragem diaria,
paramos ``nos quedamos`` em albergues de peregrinos localizados em pueblos
pelo caminho. No albergue do pueblo de Ventosa conhecemos um velho, que
apos percorrer o caminho de Santiago inacreditaveis treze vezes nos dava
bons conselhos e sabias dicas. A melhor delas foi dizer que para aquele
que fez muitas vezes, o Caminho de Santiago\index{Caminho de Santiago} era como o Caminho da Vida e
como a vida tem as suas fases Infantil, Adolescente, Adulta e Madura.
Assim, teriamos de St Jean-Pied-de-Port a Logrono a fase infantil:
aprendemos a andar e enfrentamos muitos problemas para caminhar. De
Logrono a Burgos\index{Burgos} a fase adolescente: ainda enfrentamos problemas para
caminhar mas ja adquirimos um pouco de desenvoltura. De Burgos a Leon\index{Leon} a
fase adulta: maior facilidade para enfrentar as dificuldades da caminhada
e adquirido maior desenvoltura para caminhar. E, por fim, de Leon a
Santiago de Compostela, a fase madura: caminha-se com experiencia e maior
sabedoria em relacao as dificuldades.

<br><br>

<b>3)</b> Sao Cipriano\index{Sao Cipriano}, na historia a qual tenho conhecimento, viveu em Cartago \index{Cartago} e
durante muitos anos praticou magia com a finalidade de prejudicar. Obteve
conhecimentos incriveis para a sua epoca e as usou para propositos ruins,
porem num determinado momento voltou sua força e poder para propositos
beneficos. Seja como for, o que sei de Sao Cipriano fez-me respeita-lo
bastante e entender que, o que mais eu puder saber de sua historia de vida
ira me acrescentar. Ontem, perto do pueblo de Reliegos, soube que la havia
uma igreja com o nome de São Cipriano\index{Sao Cipriano}, achei curiosa esta coincidencia,
pois ate o dia anterior eu nao tinha o costume de ler guia algum sobre os
pueblos pelo qual passava, mais ainda quando falavam de igrejas. Paramos
no bar do pueblo para o lanche do almoco e apos passarmos frente a igreja
a missa estava para comecar, entramos e a assistimos. Tirei algumas fotos
da imagem de Sao Cipriano e agradeci pela sorte de passar ali justo no
momento certo.


 
 
  
